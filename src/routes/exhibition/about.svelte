    <h1>ABOUT "INTO INFINITY"</h1>
    <div style="text-align:center;width:480px;padding:10px;margin:auto;">

        <p>
        INTO INFINITY: an exploration of on and<br/> on and on and on ...
        </p>

        <p>
        Explore the wide possibilities of forever! INTO INFINITY is an art and music exhibition presented by DUBLAB and Creative Commons. It is built on audio loops and circular canvases that are available online to the public for sharing, remixing, and resubmission. Stay tuned for live exhibition dates and website developments.
        </p>

        <p>
        "EYE"<br/>
        We distributed white 12-inch paper circles to our community of inspired artists to fill with vibrant visions. These infinite loops are designed to shine and blow minds.</p>
        
        <p>
        "EAR"<br/>
        We asked our music-minded friends to create 8-second audio loops filled with any sound texture they desired. These infinite loops are designed to expand in open ears.</p>
        <p>
        
        All of the works that comprise INTO INFINITY are available to the public under a Creative Commons <a href="http://creativecommons.org/licenses/by-nc/3.0/us/">Attribution-Noncommercial</a> copyright license. This means that you are free to share them and remix them for any noncommercial purpose you wish. We can't wait to see what you do with these infinite loops!
        </p>
